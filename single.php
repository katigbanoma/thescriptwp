                            <p><span class="mh-dropcap">T</span> he office of the Attorney General of the Federation (AGF) yesterday
                                completed the transmission of record of proceedings at the Code of Conduct Tribunal (CCT)
                                to the Court of Appeal in Abuja in relation to the appeal it filed against the acquittal
                                of Senate President Bukola Saraki by the CCT.</p>

                            <p>The record, and briefs of argument to be filed by parties, form what the Court of Appeal will
                                consider and review in deciding the appeal filed by the AGF (on behalf of the Federal Government)
                                against the CCT judgment of June 14. The number is: CA/A/460/2017. With the appeal now entered
                                and number allocated, the appellant, by the Rules of the Court of Appeal, It was learnt yesterday
                                that the transmission of the record started on Wednesday, but was completed yesterday, following
                                which the Appeal Court entered the appeal on its list and allocated a number to it. </p>

                            <p>is to proceed to file its brief, serve on the respondent, who in turn, will file its response
                                and serve the appellant (the process of briefs exchange), following which the court will
                                fix a date for hearing of the appeal. In a notice of appeal it filed on June 20, 2017 the
                                appellant stated that the judgment “effectively” overruled previous decisions of the Court
                                of Appeal delivered with respect to Saraki’s trial and other criminal cases.</p>

                            <p>“The judgment of the lower tribunal is unwarranted, unreasonable and against the weight of evidence,”
                                the notice of appeal read in part. The notice of appeal was signed by the lead prosecuting
                                counsel,  Rotimi Jacobs (SAN), and an Assistant Chief State Counsel in the Federal Ministry
                                of Justice, Pius Akutah. The appellant described the judgment as “unconstitutional and without
                                jurisdiction.” It stated that the CCT erred in law by upholding Saraki’s no-case submission
                                “when the onus of proof” was on the Senate President to show “that there was no infraction
                                in the Code of Conduct Forms.</p>
                            <p>

                            </p>

                            <p>The appellant argued that: “By the provisions of paragraphs 11 (2), (3) and (13) of Part 1, 5th
                                Schedule to the Constitution of the Federal Republic of Nigeria, 1999 (as amended), once
                                the Code of Conduct form filled by the public officer is investigated and found to</p>

                            <p>be false or that some assets are beyond the legitimate income of the public officer or that the
                                assets were acquired by means of corrupt practices, the public officer concerned is deemed
                                to have breached the Code of Conduct and it is for him to show to the tribunal that there
                                is no infraction in the form.</p>
